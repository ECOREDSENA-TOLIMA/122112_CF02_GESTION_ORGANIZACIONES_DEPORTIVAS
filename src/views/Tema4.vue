        p.mt-3 El mercadeo deportivo se entiende como el arte para descubrir e identificar claramente las necesidades y expectativas del mercado, de manera tal que maximice la satisfacción de necesidades y expectativas de practicantes, aficionados, patrocinadores y la sociedad en general, con el fin de lograr que practicantes y seguidores se unan a las causas para generar beneficio social.
